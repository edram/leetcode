import cases from "./cases.json";
import maxScore from "./maxScore";

test("maximum number of operations with the same score i", () => {
  cases.forEach((testCase, index) => {
    console.log(`正在执行第 ${index + 1} 个测试用例\n`);
    console.time("time");
    const result = maxScore(testCase.input.nums, testCase.input.x);
    console.timeEnd("time");
    console.log(
      `input: ${testCase.input} , output: ${testCase.output} ==> ${result} \n`
    );

    expect(result).toEqual(testCase.output);
  });
});
